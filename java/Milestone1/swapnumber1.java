package Milestone1;

public class swapnumber1 {
    public static void main(String[] args) {


        int a = 10,b=90;
        System.out.println(a+" ");
        System.out.println(b+" ");

        int temp=a;
        a=b;
        b=temp;


        System.out.println(a+ "=a");
        System.out.println(b+" =b");
    }
}
