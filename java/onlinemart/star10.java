package onlinemart;

public class star10
{
    public static void main(String[] args) {
        for (int i = 0; i <=5 ; i++) {
            for (int j = 0; j <=i; j--) {
                System.out.print(" ");

            }
        }
        for (int i =5; i <=i ; i--) {
            System.out.print("*");

        }
        System.out.println();
    }
}
