package onlinemart;

public class star11 {



    public static void main(String[] args) {
        int m=3;
        for (int i = 0; i <=m ; i++) {
            for (int j = 0; j <=i ; j++) {
                System.out.print("** ");
            }


            System.out.println();
        }
        for (int k =m; k>0 ; k--) {
            for (int i = 0; i <k; i++) {
                System.out.print("** ");

            }
            System.out.println();
        }

    }
}
