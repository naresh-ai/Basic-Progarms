public class star3 {
    public static void main(String[] args) {

        for (int i = 5; i >=0 ; i--)
        {
            //System.out.print(" ");
            //System.out.println();
            for (int j = i; j >0; j--) {

                System.out.print(" ");
                //System.out.println("*");
                //System.out.println(" ");

            }
            for (int j = 5; j >=i; j--) {
                System.out.print("*");
            }
            System.out.println();
        }

    }
}











































